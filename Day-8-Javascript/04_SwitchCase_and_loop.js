// Switch case.
let a = 1;

switch (a) {
  case 1:
    console.log("Sunday");
    break;

  case 2:
    console.log("Monday");
    break;
  case 3:
    console.log("Tuesday");
    break;
  default:
    console.log("Kuch bhi");
}

// For loop.

for (let i = 0; i < 5; i++){
  console.log("hello ", i);
}


// while loop.

let i = 5;

while (i > 0) {
  console.log("I am while loop ", i);
  i--;
}


// do while
let m = 5;
